# list -> array, mengakses dengan menggunakan index

data_list = ["ucup", "otong", "dudung"]

print(data_list[0])


# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    "cp": "ucup",
    "tg": "otong",
    "dg": "duding",
    "nmbr": 100,
    "list": data_list,
}
print(data_dict["tg"])
print(data_dict["nmbr"])
print(data_dict["list"])
